z1 = complex(-1, 1)
z2 = complex(-4, -2)

# Сумма
sum_result = z1 + z2
print("Сумма:", sum_result)

# Разность
diff_result = z1 - z2
print("Разность:", diff_result)

# Произведение
prod_result = z1 * z2
print("Произведение:", prod_result)

# Частное
quot_result = z1 / z2
print("Частное:", quot_result)